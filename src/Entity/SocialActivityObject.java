package Entity;

/**
 *
 * @author vutran
 */
public class SocialActivityObject
{
    private String sImageName;
    private String sSocialName;
    private String sValue;

    /**
     * @return the sSocialName
     */
    public String getsSocialName()
    {
        return sSocialName;
    }

    /**
     * @param sSocialName the sSocialName to set
     */
    public void setsSocialName(String sSocialName)
    {
        this.sSocialName = sSocialName;
    }

    /**
     * @return the sValue
     */
    public String getsValue()
    {
        return sValue;
    }

    /**
     * @param sValue the sValue to set
     */
    public void setsValue(String sValue)
    {
        this.sValue = sValue;
    }

    /**
     * @return the sImageName
     */
    public String getsImageName()
    {
        return sImageName;
    }

    /**
     * @param sImageName the sImageName to set
     */
    public void setsImageName(String sImageName)
    {
        this.sImageName = sImageName;
    }
}
